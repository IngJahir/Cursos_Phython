# Operadores relacionales: Se utilizan para comparar dos operandos

# Operador igual
print(10 == 12)

# Operador diferente con numero y con caracteres 
print(10 != 12)
print('azul' != 'azul')

# Operador menor y mayor que
print(8 < 9)
print(9 > 10)

# Operador menor-igual y mayor-igual que
print(8 <= 9)
print(9 >= 10)

#Operacion con listas
lista_uno = [1 , 2 , 3]
lista_dos = [1 , 2 , 3]
print(lista_uno == lista_dos)

#Operacion con tuplas
tupla_uno = (1,2,'TEST')
tupla_dos = (1,2,'TEST')
print(tupla_uno != tupla_dos)


